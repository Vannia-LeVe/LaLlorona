    private void btnSelectJsonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectJsonActionPerformed
 FileDialog fileDialog = new FileDialog(this, "Select json file", FileDialog.LOAD);
        fileDialog.setVisible(true);
        
        String directory = fileDialog.getDirectory();
        String filename = fileDialog.getFile();
        
        if(filename != null){
            File selectedFile = new File(directory, filename);
        try (BufferedReader reader = new BufferedReader(new FileReader(selectedFile))) {
            StringBuilder content = new StringBuilder();
            String line;
            while ((line = reader.readLine()) != null) {
                content.append(line).append("\n");
                }
            String contenido = content.toString();
            Account a = Account.deserialized(contenido);
            
            lblProduct.setText(a.getProduct());
            DefaultTableModel model =(DefaultTableModel) tblTransactions.getModel();
            model.setRowCount(0);
            SimpleDateFormat dateFormat= new SimpleDateFormat("dd'/'MM'/'yyyy");// se puede cambiar el formato del a√±o
         
                            for(Transactions t: a.getTransactions()){
                            model.addRow(new Object[]{
                                dateFormat.format(t.getDate()),
                                t.getDescription(),
                                t.getReference(),
                                "$" +t.getAmount(),
                                t.getType(),                            
                            });           
                         
        }
 }
        catch (IOException ex) {
                        ex.printStackTrace();
                    }
        
    }      }//GEN-LAST:event_btnSelectJsonActionPerformed

